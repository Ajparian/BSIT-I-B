public class REBIAS_ACTIVITY_I {

    public static void main (String[] args) {

        int x = 30;
        int y = 40;
        int z = 50;
        System.out.println(x*y*z);
        System.out.println(x+y+z);
        System.out.println(x-y-z);
        System.out.println(x/y/z);
        
    }
}
